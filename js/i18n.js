// ── Core i18n Engine ──────────────────────────────────────
const I18N = {
    currentLang: localStorage.getItem('lojikon_lang') || 'tr',

    t(keyPath) {
        const keys = keyPath.split('.');
        let val = TRANSLATIONS[this.currentLang];
        for (const k of keys) {
            if (val == null) return keyPath;
            val = val[k];
        }
        return val ?? keyPath;
    },

    apply() {
        // Text nodes
        document.querySelectorAll('[data-i18n]').forEach(el => {
            el.textContent = this.t(el.dataset.i18n);
        });
        // HTML nodes (for <br>, <span> etc.)
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            el.innerHTML = this.t(el.dataset.i18nHtml);
        });
        // Placeholder
        document.querySelectorAll('[data-i18n-ph]').forEach(el => {
            el.placeholder = this.t(el.dataset.i18nPh);
        });
        // <html lang> attribute
        document.documentElement.lang = this.currentLang;
        // Update toggle button
        const btn = document.getElementById('lang-toggle');
        if (btn) btn.textContent = this.currentLang === 'tr' ? 'EN' : 'TR';
        // Re-render config-driven spec cards if renderer is loaded
        if (window.renderSpecsForLang) window.renderSpecsForLang(this.currentLang);
    },

    setLang(lang) {
        this.currentLang = lang;
        localStorage.setItem('lojikon_lang', lang);
        this.apply();
    },

    init() {
        this.apply();
        const btn = document.getElementById('lang-toggle');
        if (btn) {
            btn.addEventListener('click', () => {
                this.setLang(this.currentLang === 'tr' ? 'en' : 'tr');
            });
        }
    },
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
